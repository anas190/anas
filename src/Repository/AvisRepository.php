    public function findCount($name){
        $entityManager=$this->getEntityManager();
        $query=$entityManager
            ->createQuery("SELECT Count(s.idInvitation) as idInvitation FROM APP\Entity\Invitation s JOIN s.Equipe c WHERE c.idEquipe=:name ")
            ->setParameter('name',$name);
            return $query->getSingleScalarResult();
    }
    public function find_Nb_Rec_Par_Status($type)
    {

        $em = $this->getEntityManager();

        $query = $em->createQuery(
            'SELECT DISTINCT  count(r.note) FROM   App\Entity\Avis r  where r.note = :note   '
        );
        $query->setParameter('note', $type);
        return $query->getResult();
    }
